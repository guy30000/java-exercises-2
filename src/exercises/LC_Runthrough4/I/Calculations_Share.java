package exercises.LC_Runthrough4.I;

public class Calculations_Share {

    public double areaOfCircle(double radius){
        double area = 3.14* radius * radius;
        return area;
    }

    public double areaOfRectangle(double l, double w ){
        double area = l*w;
        return area;
    }

    public double areaOfCube(double l, double w, double d){
        double area = l*w*d;
        return area;
    }



}
